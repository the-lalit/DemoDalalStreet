package operatorsProg;

public class AssignmentOperator {
	
	public static void main(String[] args) 
	
	{
		  int a = 10;         // 10
	System.out.println (a);  
	a+=3;                     // 10+3=13
	System.out.println (a);  
    a-=2;                     // 13-2 = 11
	System.out.println (a);
    a*=5;                   // 11*5 = 55
    System.out.println (a);
    a/=5;                     // 55/5= 11
    System.out.println (a);
	}
  
}
