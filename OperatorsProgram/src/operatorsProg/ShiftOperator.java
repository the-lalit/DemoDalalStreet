package operatorsProg;

public class ShiftOperator {

	public static void main (String args[])
	{
		//left shift
		System.out.println (20<<3); // 20*2^3 = 160
		System.out.println (10<<2); // 10*2^3 = 80
		System.out.println (30<<4); // 30*2^4 = 480
		System.out.println (5<<5); // 5*2^5 = 160
		
		//Right Shift
		System.out.println (40>>3); // 
		System.out.println (50>>2); //
	}
}
